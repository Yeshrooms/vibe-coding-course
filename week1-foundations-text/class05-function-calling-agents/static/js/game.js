// ⚡ INPUT HANDLERS (Listening to Your Fingers)
// -------------------------------------------------------------------------

function handlePhysicalKeyPress(e) {
    if (game_over || isProcessing) return;
    if (e.altKey || e.ctrlKey || e.metaKey) return;

    const key = e.key.toUpperCase();
    
    if (key === "ENTER") {
        submitCurrentGuess();
    } else if (key === "BACKSPACE" || key === "DELETE") {
        removeLetter();
    } else if (/^[A-Z]$/.test(key)) {
        // Sparkles centered on physical tiles
        const activeTile = document.getElementById(`tile-${guessesUsed}-${currentGuess.length}`);
        if (activeTile) {
            const rect = activeTile.getBoundingClientRect();
            triggerSparkles(rect.left + rect.width / 2, rect.top + rect.height / 2);
        }
        appendLetter(key);
    }
}

function handleVirtualKeyPress(key) {
    if (game_over || isProcessing) return;
    
    if (key === "ENTER") {
        submitCurrentGuess();
    } else if (key === "BACKSPACE") {
        removeLetter();
    } else {
        appendLetter(key);
    }
}

function appendLetter(letter) {
    if (currentGuess.length < 5) {
        currentGuess += letter;
        soundType(); // sound play
        updateTiles();
    }
}

function removeLetter() {
    if (currentGuess.length > 0) {
        currentGuess = currentGuess.slice(0, -1);
        soundDelete(); // sound play
        updateTiles();
    }
}

function updateTiles() {
    for (let c = 0; c < 5; c++) {
        const tile = document.getElementById(`tile-${guessesUsed}-${c}`);
        if (!tile) continue;
        
        const front = tile.querySelector(".tile-front");
        const letter = currentGuess[c] || "";
        
        front.textContent = letter;
        
        if (letter) {
            tile.classList.add("active");
        } else {
            tile.classList.remove("active");
        }
    }
}

