// 🎵 WEB AUDIO SYNTH ENGINE (Self-Contained Browser Sound Effects)
// -------------------------------------------------------------------------
let isMuted = false;
let audioCtx = null;

/**
 * Gets or initializes the standard browser audio context.
 * Autoplay security blocks initial context start until a real interaction (click) occurs.
 */
function getAudioCtx() {
    if (!audioCtx) {
        audioCtx = new (window.AudioContext || window.webkitAudioContext)();
    }
    if (audioCtx.state === "suspended") {
        audioCtx.resume();
    }
    return audioCtx;
}

/**
 * Synthesizes and plays a clean customized retro synthesizer tone.
 * @param {number} freq - Pitch frequency in Hz
 * @param {string} type - Wave type: 'sine' | 'triangle' | 'sawtooth' | 'square'
 * @param {number} duration - Time decay in seconds
 * @param {number} volume - Output amplitude
 */
function playTone(freq, type, duration, volume = 0.1) {
    if (isMuted) return;
    try {
        const ctx = getAudioCtx();
        const osc = ctx.createOscillator();
        const gain = ctx.createGain();
        
        osc.type = type;
        osc.frequency.setValueAtTime(freq, ctx.currentTime);
        
        // Smooth exponential volume decay envelope to avoid harsh popping noises
        gain.gain.setValueAtTime(volume, ctx.currentTime);
        gain.gain.exponentialRampToValueAtTime(0.0001, ctx.currentTime + duration);
        
        osc.connect(gain);
        gain.connect(ctx.destination);
        
        osc.start();
        osc.stop(ctx.currentTime + duration);
    } catch (e) {
        console.warn("Audio Context blocked or uninitialized:", e);
    }
}

// 🔊 Sound Hooks
function soundType() {
    playTone(600, "sine", 0.05, 0.06); // quick vintage tack click
}

function soundDelete() {
    playTone(280, "sine", 0.06, 0.06); // lower tap for delete
}

function soundError() {
    playTone(120, "sawtooth", 0.25, 0.1); // low retro buz for wrong actions
}

function soundFlip(colIdx) {
    // Generate an ascending pitch click depending on the column index!
    const baseFreq = 380;
    playTone(baseFreq + (colIdx * 65), "triangle", 0.07, 0.05);
}

async function soundVictory() {
    if (isMuted) return;
    // Beautiful ascending Major 9th chime chord cascade
    const chords = [261.63, 329.63, 392.00, 493.88, 523.25, 659.25, 783.99]; // C4, E4, G4, B4, C5, E5, G5
    for (let i = 0; i < chords.length; i++) {
        playTone(chords[i], "sine", 0.35, 0.08);
        await sleep(90);
    }
}

function toggleMute() {
    isMuted = !isMuted;
    const btn = document.getElementById("btn-toggle-sound");
    if (isMuted) {
        btn.textContent = "🔇";
        btn.title = "Unmute Sounds";
    } else {
        btn.textContent = "🔊";
        btn.title = "Mute Sounds";
        // Play visual check tone
        playTone(440, "sine", 0.15, 0.08);
    }
}

