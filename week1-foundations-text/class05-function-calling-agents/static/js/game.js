// 🧱 BOARD & KEYBOARD BUILDERS (Drawing the Interface)
// -------------------------------------------------------------------------

function buildBoard() {
    const board = document.getElementById("wordle-board");
    board.innerHTML = ""; // Wipe out any previous boards
    
    for (let r = 0; r < maxGuesses; r++) {
        const row = document.createElement("div");
        row.className = "board-row";
        row.id = `row-${r}`;
        
        for (let c = 0; c < 5; c++) {
            const tile = document.createElement("div");
            tile.className = "tile";
            tile.id = `tile-${r}-${c}`;
            
            const inner = document.createElement("div");
            inner.className = "tile-inner";
            
            const front = document.createElement("div");
            front.className = "tile-front";
            
            const back = document.createElement("div");
            back.className = "tile-back";
            
            inner.appendChild(front);
            inner.appendChild(back);
            tile.appendChild(inner);
            row.appendChild(tile);
        }
        board.appendChild(row);
    }
}

function buildKeyboard() {
    const keyboard = document.getElementById("qwerty-keyboard");
    keyboard.innerHTML = "";
    
    const rows = [
        ["Q", "W", "E", "R", "T", "Y", "U", "I", "O", "P"],
        ["A", "S", "D", "F", "G", "H", "J", "K", "L"],
        ["ENTER", "Z", "X", "C", "V", "B", "N", "M", "BACKSPACE"]
    ];
    
    rows.forEach(rowKeys => {
        const rowDiv = document.createElement("div");
        rowDiv.className = "keyboard-row";
        
        rowKeys.forEach(key => {
            const btn = document.createElement("button");
            btn.className = "key";
            btn.textContent = key === "BACKSPACE" ? "⌫" : key;
            btn.setAttribute("data-key", key);
            
            if (key === "ENTER" || key === "BACKSPACE") {
                btn.classList.add("wide-key");
            }
            
            btn.addEventListener("click", (e) => {
                triggerSparkles(e.clientX, e.clientY); // Dust sparkles!
                handleVirtualKeyPress(key);
            });
            rowDiv.appendChild(btn);
        });
        
        keyboard.appendChild(rowDiv);
    });
}

