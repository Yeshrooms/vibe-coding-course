function showGameOverModal(status, guesses, secret, critique) {
    const modal = document.getElementById("game-over-modal");
    const emoji = document.getElementById("modal-emoji");
    const title = document.getElementById("modal-title");
    const statGuesses = document.getElementById("modal-stat-guesses");
    const statWord = document.getElementById("modal-stat-word");
    const critiqueText = document.getElementById("modal-critique-text");
    
    if (status === "WON") {
        emoji.textContent = "🎉";
        title.textContent = "Victory!";
        title.className = "accent-text";
        statGuesses.textContent = `${guesses} / 6`;
    } else {
        emoji.textContent = "😔";
        title.textContent = "Out of Tries!";
        title.className = "";
        statGuesses.textContent = "X / 6";
    }
    
    statWord.textContent = secret;
    critiqueText.textContent = critique;
    
    modal.classList.remove("hidden");
}

function closeModal() {
    const modal = document.getElementById("game-over-modal");
    modal.classList.add("hidden");
}
