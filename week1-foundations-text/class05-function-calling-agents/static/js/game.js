/**
 * 💡 EXTRA FEATURE: Calls the new Flask endpoint /api/hint, streams logs,
 *    and overrides Game Master speech dialogue with a witty, encouraging clue!
 */
async function askForHint() {
    if (game_over || isProcessing || !sessionId) {
        soundError();
        return;
    }
    
    isProcessing = true;
    document.getElementById("loading-spinner").classList.remove("hidden");
    appendLog("system", "Initiating stand-alone completion. Querying Gemini hint advisor...");
    
    try {
        const response = await fetch("/api/hint", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({ session_id: sessionId })
        });
        
        if (!response.ok) throw new Error(await response.text());
        
        const data = await response.json();
        document.getElementById("loading-spinner").classList.add("hidden");
        
        // Stagger logs sequentially for real-time calculation feel
        for (const log of data.logs) {
            appendLog(log.type, log.content);
            await sleep(550);
        }
        
        // Show clue in the avatar speech bubble
        document.getElementById("ai-response-text").textContent = data.hint;
        
        // Sparkle burst around the hint button itself
        const hintBtn = document.getElementById("btn-ask-hint");
        if (hintBtn) {
            const rect = hintBtn.getBoundingClientRect();
            triggerSparkles(rect.left + rect.width / 2, rect.top + rect.height / 2);
        }
        
        // Play nice high-pitched chime indicating a successful clue discovery
        playTone(783.99, "sine", 0.25, 0.07); // G5 chime
        
    } catch (e) {
        appendLog("system", `Hint generator failed: ${e.message}`);
        document.getElementById("loading-spinner").classList.add("hidden");
        soundError();
    } finally {
        isProcessing = false;
    }
}

