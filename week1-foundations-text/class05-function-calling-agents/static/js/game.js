// 🤖 AGENT LOGS & VISUAL CONSOLE TIMING
// -------------------------------------------------------------------------

function appendLog(type, content) {
    const consoleLogs = document.getElementById("brain-console");
    const logEntry = document.createElement("div");
    logEntry.className = `log-entry ${type}`;
    
    const timestamp = new Date().toLocaleTimeString([], { hour12: false });
    
    let badgeText = "THOUGHT";
    if (type === "system") badgeText = "SYSTEM";
    if (type === "tool_call") badgeText = "TOOL_CALL";
    if (type === "tool_response") badgeText = "TOOL_OUT";
    if (type === "text") badgeText = "SPEECH";
    
    logEntry.innerHTML = `
        <span class="log-timestamp">[${timestamp}]</span>
        <span class="log-badge">${badgeText}</span>
        <span class="log-text">${content}</span>
    `;
    
    consoleLogs.appendChild(logEntry);
    consoleLogs.scrollTop = consoleLogs.scrollHeight;
}

