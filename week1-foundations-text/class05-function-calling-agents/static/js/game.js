async function startNewGame() {
    game_over = false;
    isProcessing = true;
    currentGuess = "";
    guessesUsed = 0;
    keyColors = {};
    
    document.getElementById("guess-counter").textContent = "Guess 1 / 6";
    document.getElementById("ai-response-text").textContent = "Synthesizing AI Game Master context...";
    document.getElementById("loading-spinner").classList.remove("hidden");
    
    const inspector = document.getElementById("prompt-inspector");
    if (inspector) inspector.classList.add("collapsed");
    
    document.querySelectorAll(".key").forEach(btn => {
        btn.className = "key";
        if (btn.getAttribute("data-key") === "ENTER" || btn.getAttribute("data-key") === "BACKSPACE") {
            btn.classList.add("wide-key");
        }
    });
    
    const consoleLogs = document.getElementById("brain-console");
    consoleLogs.innerHTML = "";
    appendLog("system", "Wiping previous registers. Allocating new game environment...");
    
    buildBoard();
    
    try {
        const response = await fetch("/api/start", {
            method: "POST",
            headers: { "Content-Type": "application/json" }
        });
        
        const data = await response.json();
        sessionId = data.session_id;
        maxGuesses = data.max_guesses;
        
        appendLog("system", "Session ID generated successfully.");
        appendLog("thought", "Imported dictionary and parsed hidden target word.");
        appendLog("thought", "System prompt loaded. Available functions declared to Gemini.");
        appendLog("text", "Waiting for player's first input guess.");
        
        const systemPromptExplanation = `You are the AI Wordle Game Master. The player is trying to guess a secret 5-letter word.
The SECRET word is: '[HIDDEN_SECRET_WORD]'
The player has a maximum of 6 guesses.

Rules of the game you must enforce:
1. When the player makes a guess, you MUST call the validate_word tool with their guess.
2. Present results using the returned emojis (🟩/🟨/⬜) and explain letter outcomes.
3. Call end_game upon victory or exhaustion of attempts.`;

        document.getElementById("prompt-text").textContent = systemPromptExplanation;
        document.getElementById("ai-response-text").textContent = data.greeting;
        
        // Initial soft chime indicating boot sequence completed
        playTone(523.25, "sine", 0.15, 0.05); // C5
        
    } catch (e) {
        appendLog("system", "Could not reach Flask server. Is local environment active?");
        document.getElementById("ai-response-text").textContent = "Oh no! Server offline. Please start 'python app.py' in your terminal and reload.";
    } finally {
        document.getElementById("loading-spinner").classList.add("hidden");
        isProcessing = false;
    }
}

async function submitCurrentGuess() {
    if (currentGuess.length !== 5) {
        const row = document.getElementById(`row-${guessesUsed}`);
        if (row) {
            row.classList.add("shake");
            soundError(); // sound play
            appendLog("system", `Rejected guess '${currentGuess}': Must be exactly 5 letters.`);
            setTimeout(() => row.classList.remove("shake"), 500);
        }
        return;
    }
    
    isProcessing = true;
    document.getElementById("loading-spinner").classList.remove("hidden");
    appendLog("system", `Guess '${currentGuess}' submitted. Directing agent...`);
    
    try {
        const response = await fetch("/api/guess", {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
                session_id: sessionId,
                guess: currentGuess
            })
        });
        
        if (!response.ok) {
            throw new Error(await response.text());
        }
        
        const data = await response.json();
        document.getElementById("loading-spinner").classList.add("hidden");
        
        for (const log of data.logs) {
            appendLog(log.type, log.content);
            await sleep(550);
        }
        
        const rowIdx = guessesUsed;
        const colors = data.board[rowIdx].colors;
        const guessLetters = data.board[rowIdx].guess;
        
        for (let c = 0; c < 5; c++) {
            const tile = document.getElementById(`tile-${rowIdx}-${c}`);
            const back = tile.querySelector(".tile-back");
            
            back.textContent = guessLetters[c];
            back.className = `tile-back ${colors[c]}`;
            
            tile.classList.add("flipped");
            soundFlip(c); // play sound
            
            updateKeyboardKey(guessLetters[c], colors[c]);
            
            await sleep(220);
        }
        
        guessesUsed = data.guesses_used;
        document.getElementById("guess-counter").textContent = `Guess ${Math.min(guessesUsed + 1, maxGuesses)} / ${maxGuesses}`;
        document.getElementById("ai-response-text").textContent = data.reply;
        
        if (data.game_over) {
            game_over = true;
            appendLog("system", `Concluded: Target Answer was '${data.secret_word}'.`);
            
            if (data.status === "WON") {
                await sleep(500);
                for (let c = 0; c < 5; c++) {
                    const tile = document.getElementById(`tile-${rowIdx}-${c}`);
                    tile.classList.add("bounce");
                    await sleep(80);
                }
                
                // Blast sounds & canvas confetti particle layers!
                soundVictory();
                if (typeof confetti === "function") {
                    confetti({
                        particleCount: 160,
                        spread: 85,
                        origin: { y: 0.6 }
                    });
                    
                    setTimeout(() => {
                        confetti({
                            particleCount: 100,
                            spread: 120,
                            origin: { y: 0.5 }
                        });
                    }, 400);
                }
            } else {
                // Sound effect indicating defeat
                playTone(220, "sawtooth", 0.4, 0.08); // low mourning tone
            }
            
            setTimeout(() => {
                showGameOverModal(data.status, guessesUsed, data.secret_word, data.reply);
            }, 1200);
            
        } else {
            currentGuess = "";
            isProcessing = false;
        }
        
    } catch (e) {
        appendLog("system", `Execution exception: ${e.message}`);
        document.getElementById("loading-spinner").classList.add("hidden");
        isProcessing = false;
    }
}

function updateKeyboardKey(letter, color) {
    const keyBtn = document.querySelector(`.key[data-key="${letter}"]`);
    if (!keyBtn) return;
    
    const priority = { "green": 3, "yellow": 2, "gray": 1 };
    const currentScore = priority[keyColors[letter]] || 0;
    const newScore = priority[color];
    
    if (newScore > currentScore) {
        keyColors[letter] = color;
        keyBtn.classList.remove("green", "yellow", "gray");
        keyBtn.classList.add(color);
    }
}

