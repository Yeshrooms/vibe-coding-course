// ✨ EXTRA COLLATERAL EFFECTS (Visual Polish)
// -------------------------------------------------------------------------

function triggerSparkles(x, y) {
    if (!x || !y) return;
    
    const count = 10;
    for (let i = 0; i < count; i++) {
        const p = document.createElement("div");
        p.className = "sparkle-particle";
        p.style.left = `${x}px`;
        p.style.top = `${y}px`;
        
        const angle = Math.random() * Math.PI * 2;
        const speed = 20 + Math.random() * 50;
        const tx = Math.cos(angle) * speed;
        const ty = Math.sin(angle) * speed;
        
        p.style.setProperty("--tx", `${tx}px`);
        p.style.setProperty("--ty", `${ty}px`);
        
        document.body.appendChild(p);
        setTimeout(() => p.remove(), 600);
    }
}

function togglePromptInspector() {
    const inspector = document.getElementById("prompt-inspector");
    const btn = document.getElementById("btn-toggle-prompt");
    
    if (inspector.classList.contains("collapsed")) {
        inspector.classList.remove("collapsed");
        btn.textContent = "✖ Close Inspect";
        playTone(440, "sine", 0.08, 0.05);
    } else {
        inspector.classList.add("collapsed");
        btn.textContent = "📋 Inspect Prompt";
        playTone(330, "sine", 0.08, 0.05);
    }
}

